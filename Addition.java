
/**
 * This Class will hold oatMilk class and sugarSyrup in an abstract class
 * @author javierpalma
 */
public abstract class Addition {


    /**
     * This method will get the cost of an addOn
     * @return This is the abstract class that will return the cost of each add on in Money variable
     */
    public abstract Money getCost();

    /**
     * This Method will get the calories of a addOn
     * @return the calories of an add on of either sugarSyrup or OatMilk
     */
    public abstract int getCalories();




}
